    echo "ðŸ”§ DNS Records to Create:"
    echo "   Type: A"
    echo "   Name: @"
    echo "   Value: $PROXY_IP"
    echo "   TTL: 300"
    echo ""
    echo "   Type: A" 
    echo "   Name: www"
    echo "   Value: $PROXY_IP"
    echo "   TTL: 300"
    echo ""
    echo "ðŸ“ After DNS setup:"
    echo "   â€¢ Wait 5-15 minutes for DNS propagation"
    echo "   â€¢ SSL certificate will auto-issue via Let's Encrypt"
    echo "   â€¢ Check certificate: kubectl get certificates -n hq"
    echo ""
