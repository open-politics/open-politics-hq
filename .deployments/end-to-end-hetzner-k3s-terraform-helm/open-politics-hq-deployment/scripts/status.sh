PROXY_IP=$(kubectl get svc traefik -n kube-system -o jsonpath='{.status.loadBalancer.ingress[0].ip}' 2>/dev/null || echo "")
if [ -n "$PROXY_IP" ]; then
    echo "   LoadBalancer IP: $PROXY_IP"
else
    echo "   âš ï¸  LoadBalancer IP not assigned yet"
fi
