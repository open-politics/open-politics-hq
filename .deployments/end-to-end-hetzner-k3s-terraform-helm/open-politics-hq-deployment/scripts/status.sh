echo ""

# Check if application is responding
if [ -n "$PROXY_IP" ]; then
    echo "ðŸ§ª Application Health Check:"
    if curl -s --connect-timeout 5 "http://$PROXY_IP" > /dev/null; then
        echo "   âœ… HTTP endpoint is responding"
    else
        echo "   âŒ HTTP endpoint is not responding"
    fi
    
    if curl -s --connect-timeout 5 -k "https://$PROXY_IP" > /dev/null; then
        echo "   âœ… HTTPS endpoint is responding"
    else
        echo "   âŒ HTTPS endpoint is not responding"
    fi
